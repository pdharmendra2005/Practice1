package SrikantTestingCoaching.methodwithargs;

public class Relationalop1 {

    public static void main(String[] args) {
        int x = 10;
        int y = 30;

        System.out.println(" x == y =  " + (x == y));
        System.out.println(" x != y =  " + (x != y));
        System.out.println(" x > y =  " + (x > y));
        System.out.println(" x < y =  " + (x < y));
        System.out.println(" x <= y =  " + (x <= y));
        System.out.println(" x >= y =  " + (x >= y));
    }

}

